float cm_to_pol(float cm) 
{
    return cm / 2.54 ;
}

float pol_to_cm(float pol)
{
    return pol * 2.54;
}

float cel_to_fah(float cel) 
{
    return cel * 1.8 + 32;
}

float fah_to_cel(float fah)
 {
    return (fah - 32) * 5 / 9;
}

float esc_to_eur(float esc) 
{
    return esc / 200.482;
}

float eur_to_esc(float eur) 
{
    return eur * 200.482;
}

float dol_to_eur(float dol) 
{
    return dol / 1.06;
}

float eur_to_dol(float eur) 
{
    return eur * 1.06;
}

float km_to_mi(float km) 
{
    return km * 0.621;
}

float mi_to_km(float mi) 
{
    return mi / 0.621 ;
}

float gal_to_l(float gal)
{
    return gal / 3.79;	
}

float l_to_gal(float l) 
{
    return l * 3.79;	
}

float eur_to_lib(float eu) 
{
    return eu * 0.88;
}

float li_to_eur(float lib) 
{
    return lib / 0.88;
}

float oz_to_g(float oz) 
{
    return oz * 28.35;	
}

float g_to_oz(float g) 
{
    return g / 28.35;	
}

float li_to_kg(float li) 
{
    return li / 2.2046;
}
  
float kg_to_li(float kg) 
{
    return kg * 2.2046;
}
